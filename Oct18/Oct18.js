function removeAt(items, index) {
      let removedValue;
      if (index < 0){
            return null;
      } for (let i = 0; 0<=index.length;i++);{
            if (items[i] === index){
                  items[i] = removedValue;
                  items.pop(i)
            }
      }
      return removedValue;
}